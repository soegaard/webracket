#!/usr/bin/env node
/**
 * One-off generator for HTML element attribute tables.
 *
 * Fetches `html-element-attributes` package source, extracts
 * `htmlElementAttributes`, and writes normalized JSON + s-expression output.
 */

import fs from "node:fs/promises";
import path from "node:path";
import vm from "node:vm";

const DEFAULT_VERSION = "3.1.0";
const DEFAULT_OUT_JSON = "lib/web-easy/generated/html-element-attributes.json";
const DEFAULT_OUT_SEXP = "lib/web-easy/spec/html-element-attributes.sexp";

function usage() {
  console.log(`Usage:
  node tools/fetch-html-element-attributes.mjs [options]

Options:
  --version <semver>    Package version (default: ${DEFAULT_VERSION})
  --source-url <url>    Override source URL
  --out-json <path>     Output JSON path (default: ${DEFAULT_OUT_JSON})
  --out-sexp <path>     Output s-expression path (default: ${DEFAULT_OUT_SEXP})
  --help                Show this help
`);
}

function parseArgs(argv) {
  const args = {
    version: DEFAULT_VERSION,
    sourceUrl: null,
    outJson: DEFAULT_OUT_JSON,
    outSexp: DEFAULT_OUT_SEXP,
  };
  for (let i = 0; i < argv.length; i += 1) {
    const a = argv[i];
    if (a === "--help") {
      usage();
      process.exit(0);
    } else if (a === "--version") {
      i += 1;
      args.version = argv[i];
    } else if (a === "--source-url") {
      i += 1;
      args.sourceUrl = argv[i];
    } else if (a === "--out-json") {
      i += 1;
      args.outJson = argv[i];
    } else if (a === "--out-sexp") {
      i += 1;
      args.outSexp = argv[i];
    } else {
      throw new Error(`Unknown option: ${a}`);
    }
  }
  return args;
}

function defaultSourceUrl(version) {
  return `https://cdn.jsdelivr.net/npm/html-element-attributes@${version}/index.js`;
}

async function fetchText(url) {
  const res = await fetch(url);
  if (!res.ok) {
    throw new Error(`Failed to fetch ${url}: ${res.status} ${res.statusText}`);
  }
  return await res.text();
}

function extractTable(moduleSource) {
  const re = /export\s+const\s+htmlElementAttributes\s*=\s*({[\s\S]*?})\s*;?\s*(?:export\s+default[\s\S]*)?$/m;
  const m = moduleSource.match(re);
  if (!m) {
    throw new Error("Could not find `htmlElementAttributes` export in source");
  }
  const objectExpr = `(${m[1]})`;
  const table = vm.runInNewContext(objectExpr, Object.create(null));
  if (!table || typeof table !== "object" || Array.isArray(table)) {
    throw new Error("Parsed table is not an object");
  }
  return table;
}

function normalizeTable(raw) {
  const keys = Object.keys(raw).sort((a, b) => a.localeCompare(b));
  const out = {};
  for (const key of keys) {
    const attrs = raw[key];
    if (!Array.isArray(attrs)) {
      continue;
    }
    const uniq = Array.from(new Set(attrs.filter((v) => typeof v === "string")));
    uniq.sort((a, b) => a.localeCompare(b));
    out[key] = uniq;
  }
  return out;
}

function sexpEscape(s) {
  return s.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
}

function toSexp(table, meta) {
  const lines = [];
  lines.push(";; Generated by tools/fetch-html-element-attributes.mjs");
  lines.push(";; One-off snapshot for web-easy element macro generation.");
  lines.push("(html-element-attributes");
  lines.push(`  (meta (source-url "${sexpEscape(meta.sourceUrl)}") (version "${sexpEscape(meta.version)}"))`);
  lines.push("  (table");
  for (const [tag, attrs] of Object.entries(table)) {
    const attrItems = attrs.map((a) => `"${sexpEscape(a)}"`).join(" ");
    lines.push(`    ("${sexpEscape(tag)}" (${attrItems}))`);
  }
  lines.push("  ))");
  return `${lines.join("\n")}\n`;
}

async function writeFileEnsured(outPath, content) {
  const abs = path.resolve(outPath);
  await fs.mkdir(path.dirname(abs), { recursive: true });
  await fs.writeFile(abs, content, "utf8");
  return abs;
}

async function main() {
  const args = parseArgs(process.argv.slice(2));
  const sourceUrl = args.sourceUrl || defaultSourceUrl(args.version);

  const source = await fetchText(sourceUrl);
  const raw = extractTable(source);
  const table = normalizeTable(raw);

  const jsonPayload = {
    meta: {
      sourceUrl,
      version: args.version,
    },
    table,
  };

  const jsonOut = await writeFileEnsured(
    args.outJson,
    `${JSON.stringify(jsonPayload, null, 2)}\n`
  );
  const sexpOut = await writeFileEnsured(
    args.outSexp,
    toSexp(table, { sourceUrl, version: args.version })
  );

  console.log(`Wrote JSON: ${jsonOut}`);
  console.log(`Wrote S-expression: ${sexpOut}`);
}

main().catch((err) => {
  console.error(err.message);
  process.exit(1);
});
